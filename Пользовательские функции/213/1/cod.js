function sumArrayElements(array) {
	let sum = 0;
	for (let elem of array) {
		sum += elem;
	}
	return sum;
}	
function divideSums(array1, array2) {
	let sum1 = sumArrayElements(array1);
	let sum2 = sumArrayElements(array2);
	if (sum2 === 0) {
		throw new Error("Division by zero");
	}
	return sum1 / sum2;
}
